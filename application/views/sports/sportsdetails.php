    <script>
        function getteam()
        {
            
            var checkboxes = document.querySelectorAll('input[name="team"]:checked');
            var values = [];
            checkboxes.forEach(function(checkbox) 
            {
                values.push(checkbox.value);
            });
            
            team = JSON.stringify(values);
            alert(team)

            $.ajax({
            url:"<?php echo site_url()?>/filtercontroller/getmatch/"+<?php echo $spoartsid ?>,
            method:"POST",
            data:
            {
                teamname:team
            },
            success:function(data)
            {
                $('#showmatch').html(data);
            }
        });
        }
